-- Empenhos por UG/Fonte/GND/Mês — {SCHEMA_ANO}
-- Exclui eventos de anulação/reforço internos (não representam empenho líquido)
SELECT
    NE.COUG,
    UG.NOUG,
    NE.NUNE,
    NE.COUO,
    NE.COPROJETO        AS ACAO,
    NE.COFONTE,
    FR.INDESTINACAO,
    NE.CONATUREZA,
    SUBSTR(NE.CONATUREZA, 2, 1) AS GND,
    NE.COEVENTO,
    EV.NOEVENTO,
    NE.DALANCAMENTO,
    NE.VADOCUMENTO
FROM {SCHEMA_ANO}.NOTAEMPENHO NE
LEFT JOIN {SCHEMA_ANO}.UNIDADEGESTORA  UG ON NE.COUG    = UG.COUG
LEFT JOIN {SCHEMA_ANO}.FONTERECURSO    FR ON NE.COFONTE = FR.COFONTE
LEFT JOIN {SCHEMA_ANO}.EVENTO          EV ON NE.COEVENTO = EV.COEVENTO
WHERE NE.COEVENTO NOT IN (400093, 400096, 400100, 400101, 400103, 400104, 400105)
